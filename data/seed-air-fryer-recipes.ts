// Helper to add seed recipes to database
export async function seedAirFryerRecipes(db: { airFryerRecipes: { add: (recipe: AirFryerRecipe) => Promise<void> } }): Promise<void> {
  const recipes = getSeedAirFryerRecipes()

  for (const recipeData of recipes) {
    const recipe: AirFryerRecipe = {
      ...recipeData,
      id: generateId(),
      timesCooked: 0,
      createdAt: new Date(),
      updatedAt: new Date(),
    }
    await db.airFryerRecipes.add(recipe)
  }
}
